uint8_t Combine(uint8_t L, uint8_t H, uint8_t w /*Æ«ÒÆ*/)
{
    uint8_t dat;
    if (w < 16)
        dat = (H >> (8 - w)) | (L << w);
    // else
    //     printf("erro \n");
    return dat;
}

